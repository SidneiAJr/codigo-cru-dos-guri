// 1 - Crie uma função dobrar que receba um número e retorne ele dobrado.
function dobrar (){
    let n1:number = Number(prompt("Insira Um numero: "));
    let n2 :number= Number(prompt("Insira Um numero: "));
    let dobro1 = n1*2;
    let dobro2 = n2*2;
    console.log(`Dobro de ${n1} é ${dobro1}`);
    console.log(`Dobro de ${n2} é ${dobro2}`);
}
dobrar();
